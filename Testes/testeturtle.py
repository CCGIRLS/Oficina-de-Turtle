#-*- coding: utf-8 -*-
import turtle
#Turtle é um módulo em python para fazer desenhos na tela

pen = turtle.Pen()  #Constrói a caneta
pen.forward(100)    #anda 100 posições para frente
pen.right(45)       #gira o ponteiro em 45 graus para a direita
pen.forward(100)    #anda mais 100 posições para frente

# para trás caneta.backward(graus)
#para esquerda caneta.left(graus)

turtle.done() #mantém a aplicação aberta
